CREATE OR REPLACE FUNCTION public.is_admin()
 RETURNS boolean
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
begin
  return exists (
    select 1
    from users
    where user_id = auth.uid()
    and role = 'admin'
    and deleted_at is null
  );
end;
$function$
;

CREATE OR REPLACE FUNCTION public.is_manager_or_admin()
 RETURNS boolean
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
begin
  return exists (
    select 1
    from users
    where user_id = auth.uid()
    and role in ('admin', 'manager')
    and deleted_at is null
  );
end;
$function$
;


